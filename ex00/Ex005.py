n1 = int(input("Escreva um número:"))
n2 = int(input("Escreva outro numero:"))
s = n1 + n2
d = n1 / n2
e = n1 ** n2
print("A soma de {} e {} é igual a {}".format(n1, n2, s))
print("A divisão de {} e {} é igual a {}".format(n1, n2, d))
print("A exponenciação de {} e {} é igual a {}".format(n1, n2, e))
